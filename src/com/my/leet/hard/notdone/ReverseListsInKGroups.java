package com.my.leet.hard.notdone;

public class ReverseListsInKGroups {
	// https://leetcode.com/problems/reverse-nodes-in-k-group/

	public class ListNode {
		int val;
		ListNode next;

		ListNode() {
		}

		ListNode(int val) {
			this.val = val;
		}

		ListNode(int val, ListNode next) {
			this.val = val;
			this.next = next;
		}
	}

}
